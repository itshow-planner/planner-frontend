package user;

public class User {
	
	private String userName;
	private String userID;
	private String userPassword;
	private String userPasswordChecked;
	private String userAnswer;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserPasswordChecked() {
		return userPasswordChecked;
	}
	public void setUserPasswordChecked(String userPasswordChecked) {
		this.userPasswordChecked = userPasswordChecked;
	}
	public String getUserAnswer() {
		return userAnswer;
	}
	public void setUserAnswer(String userAnswer) {
		this.userAnswer = userAnswer;
	}

	
	
	
	}


